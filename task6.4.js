var k = 5;
switch (k) {
  case 1:
    console.log(k + "-погано");
    break;
  case 2:
    console.log(k + "-незадовільно");
    break;
  case 3:
    console.log(k + "-задовільно");
    break;
  case 4:
    console.log(k + "-добре");
    break;
  case 5:
    console.log(k + "-відмінно");
    break;
  default:
    console.log("помилка");
}
